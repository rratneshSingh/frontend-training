// Ternay Operator

// condition ? value1 : value2
// if condition true then output is value1 else value2
var result = true ? 3 : 4;
console.log(result);


// Switch
var value = 3;
switch (value) {
    case 3:
        console.log('value was three');
        break;
    case 5:
        console.log('value was five');
        break;
    default:
        console.log('value default')
}